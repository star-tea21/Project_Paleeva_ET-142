def search_texts(query, vectorizer, tfidf_matrix, texts_df, top_n=3):
    """
    Ищет top_n текстов, наиболее похожих на запрос.
    """
    # Очищаем и лемматизируем запрос
    query_cleaned = clean_query_text(query)
    query_lemmatized = lemmatize_query_text(query_cleaned)

    print(f"  Очищенный запрос: {query_cleaned}")
    print(f"  Лемматизированный запрос: {query_lemmatized}")

    # Векторизуем запрос
    query_vec = vectorizer.transform([query_lemmatized])

    # Считаем косинусное сходство
    similarities = cosine_similarity(query_vec, tfidf_matrix)[0]

    # Находим топ-n индексов
    top_indices = similarities.argsort()[-top_n:][::-1]

    # Формируем результаты
    results = []
    for idx in top_indices:
        results.append({
            'Текст': texts_df.iloc[idx],
            'Похожесть': round(similarities[idx], 6),
            'Метка': df['label'].iloc[idx] if 'label' in df.columns else 'unknown'
        })

    return results

