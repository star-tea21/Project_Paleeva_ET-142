import pandas as pd
import re

# 1. Загрузка очищенного от пропусков датасета
df = pd.read_csv('/content/spam_cleaned.csv', encoding='latin-1')

print("="*60)
print("ЗАГРУЗКА ДАННЫХ")
print("="*60)
print(f"Размер датасета: {df.shape[0]} строк, {df.shape[1]} столбцов")
print(f"Колонки: {df.columns.tolist()}")

# 2. Определяем нужные колонки (метка и текст)
label_col = None
text_col = None

for col in df.columns:
    if 'v1' in col or 'label' in col.lower():
        label_col = col
    if 'v2' in col or 'message' in col.lower() or 'text' in col.lower():
        text_col = col

print(f"\nНайдена колонка с метками: '{label_col}'")
print(f"Найдена колонка с текстом: '{text_col}'")

if label_col and text_col:
    df = df[[label_col, text_col]]
    df.columns = ['label', 'text']

    # ==================== УДАЛЕНИЕ PII ====================
    print("\n" + "="*60)
    print("УДАЛЕНИЕ ПЕРСОНАЛЬНЫХ ДАННЫХ (PII)")
    print("="*60)

    def remove_pii(text):
        """Удаляет персональные данные из текста (email, телефоны, URL, паспорта)"""
        text = str(text)

        # 1. Удаление email-адресов
        text = re.sub(r'\S+@\S+\.\S+', '', text, flags=re.IGNORECASE)
        text = re.sub(r'[\w\.-]+@[\w\.-]+\.\w+', '', text, flags=re.IGNORECASE)

        # 2. Удаление номеров телефонов (различные форматы)
        # Международные форматы: +7, +1, +44 и т.д.
        text = re.sub(r'\+\d{1,3}[-.\s]?\(?\d{1,4}\)?[-.\s]?\d{1,5}[-.\s]?\d{1,5}[-.\s]?\d{1,5}', '', text)
        # Номера без кода страны: 8XXXXXXXXX, 10-11 цифр
        text = re.sub(r'\b\d{10,11}\b', '', text)
        # Номера с разделителями: 123-456-7890, 123.456.7890, (123) 456-7890
        text = re.sub(r'\(?\d{3}\)?[-.\s]?\d{3}[-.\s]?\d{4}\b', '', text)
        # Британские короткие номера: 0800 123 4567, 0906 123 4567
        text = re.sub(r'\b0[0-9]{3}[-.\s]?[0-9]{3}[-.\s]?[0-9]{4}\b', '', text)

        # 4. Удаление номеров паспортов и документов
        # Российский паспорт: 2 цифры, пробел, 2 цифры, пробел, 6 цифр (пример: 45 12 345678)
        text = re.sub(r'\b\d{2}[-.\s]?\d{2}[-.\s]?\d{6}\b', '', text)
        # Загранпаспорт: 2 цифры, 7 цифр (пример: 451234567)
        text = re.sub(r'\b\d{2}[-.\s]?\d{7}\b', '', text)
        # Паспорт с серией и номером в одном блоке (10 цифр подряд)
        text = re.sub(r'\b\d{10}\b', '', text)
        # Паспорт с буквами: например, AB123456 (латинские буквы + цифры)
        text = re.sub(r'\b[A-Z]{2}\d{6}\b', '', text, flags=re.IGNORECASE)
        text = re.sub(r'\b[A-Z]{2}[-.\s]?\d{6}\b', '', text, flags=re.IGNORECASE)

        # 5. Удаление лишних пробелов (которые могли появиться после удаления)
        text = re.sub(r'\s+', ' ', text)  # Множественные пробелы -> один пробел
        text = text.strip()               # Пробелы в начале и конце

        return text

    # Применяем удаление PII ко всем текстам
    print("\nУдаление персональных данных...")
    df['text_cleaned'] = df['text'].apply(remove_pii)

    # Показываем примеры ДО и ПОСЛЕ удаления PII
    print("\n" + "="*60)
    print("ПРИМЕРЫ УДАЛЕНИЯ PII")
    print("="*60)

    shown = 0
    for i in range(len(df)):
        # Ищем строки, где есть изменения
        if df['text'].iloc[i] != df['text_cleaned'].iloc[i] and shown < 3:
            print(f"\n--- Пример {shown+1} ---")
            print(f"ДО:    {df['text'].iloc[i][:200]}")
            print(f"ПОСЛЕ: {df['text_cleaned'].iloc[i][:200]}")
            shown += 1

    if shown == 0:
        print("\nPII данные не найдены в первых строках (это хорошо)")

    # Подсчитываем статистику удалений
    print("\n" + "="*60)
    print("СТАТИСТИКА УДАЛЕНИЯ PII")
    print("="*60)

    # Считаем, сколько сообщений содержали различные типы PII
    email_count = df['text'].str.contains(r'\S+@\S+\.\S+', na=False, regex=True).sum()
    phone_count = df['text'].str.contains(r'\b\d{10,11}\b|\(\d{3}\)\s?\d{3}-\d{4}', na=False, regex=True).sum()
    passport_count = df['text'].str.contains(r'\b\d{2}\s?\d{2}\s?\d{6}\b|\b\d{10}\b', na=False, regex=True).sum()

    print(f"Сообщений с email: {email_count}")
    print(f"Сообщений с номерами телефонов: {phone_count}")
    print(f"Сообщений с номерами паспортов: {passport_count}")

    # 4. Заменяем исходный текст на очищенный (без PII)
    df['text'] = df['text_cleaned']
    df = df.drop(columns=['text_cleaned'])

    # 7. Статистика
    print("\n" + "="*60)
    print("ФИНАЛЬНАЯ СТАТИСТИКА")
    print("="*60)
    print(f"Всего сообщений: {len(df)}")
    print(f"Средняя длина текста: {df['text'].str.len().mean():.1f} символов")
    print(f"\nРаспределение классов:")
    print(df['label'].value_counts())

    # 8. Сохраняем в новый CSV-файл
    output_filename = 'spam_pii_removed.csv'
    df.to_csv(output_filename, index=False, encoding='utf-8')
    print(f"\nФайл сохранен в Colab как: '{output_filename}'")
    print(f"Полный путь: /content/{output_filename}")
else:
    print("\nНе удалось найти нужные колонки. В файле есть только:")
    print(df.columns.tolist())

print("\n" + "="*60)
print("ОБРАБОТКА ЗАВЕРШЕНА УСПЕШНО!")
