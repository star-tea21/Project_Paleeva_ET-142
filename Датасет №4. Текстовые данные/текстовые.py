def get_wordnet_pos(nltk_tag):
    """Преобразует тег NLTK в тег WordNet для корректной лемматизации"""
    if nltk_tag.startswith('J'):
        return wordnet.ADJ          # Прилагательное
    elif nltk_tag.startswith('V'):
        return wordnet.VERB         # Глагол
    elif nltk_tag.startswith('N'):
        return wordnet.NOUN         # Существительное
    elif nltk_tag.startswith('R'):
        return wordnet.ADV          # Наречие
    else:
        return wordnet.NOUN         # По умолчанию - существительное

def lemmatize_text_with_pos(text):
    """Лемматизирует текст с учётом части речи."""
    if not isinstance(text, str) or len(text.strip()) == 0:
        return ""

    # Токенизация (разбиение на слова)
    try:
        tokens = word_tokenize(text.lower())
    except LookupError:
        # Если токенизатор не работает, используем простой split
        print("⚠️ word_tokenize не работает, используется split()")
        tokens = text.lower().split()

    # POS-тегирование (определение части речи для каждого слова)
    try:
        pos_tags = pos_tag(tokens)
    except LookupError:
        # Если тегирование не работает, лемматизируем все слова как существительные
        lemmatizer = WordNetLemmatizer()
        lemmatized_tokens = [lemmatizer.lemmatize(token, pos=wordnet.NOUN) for token in tokens]
        return ' '.join(lemmatized_tokens)

    # Создаём лемматизатор
    lemmatizer = WordNetLemmatizer()

    # Лемматизируем каждое слово с учётом его части речи
    lemmatized_tokens = []
    for token, pos_tag_word in pos_tags:
        # Получаем часть речи в формате WordNet
        wn_pos = get_wordnet_pos(pos_tag_word)
        # Лемматизируем слово
        lemma = lemmatizer.lemmatize(token, pos=wn_pos)
        lemmatized_tokens.append(lemma)

    # Возвращаем текст, собранный обратно
    return ' '.join(lemmatized_tokens)

